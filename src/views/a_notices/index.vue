              :key="item.key"
              :label="item.display_name"
              :value="item.key"
            />
          </el-select>
        </el-form-item>
        <el-form-item label="公告内容" v-show="temp.content_type == 1">
          <el-input
            v-model="temp.remark"
            :autosize="{ minRows: 2, maxRows: 4 }"
            type="textarea"
          />
        </el-form-item>
        <div v-show="temp.content_type == 0" class="margin-left: 50px">
          <el-form-item>
            <el-alert
              title="设置了链接后，将不再显示文本内容"
              type="success"
            ></el-alert>
          </el-form-item>

          <el-form-item label="文章链接">
            <el-input v-model="temp.content" />
          </el-form-item>
          <el-form-item label="链接操作">
            <el-button @click="dialogFormVisible = false" type="primary">
              新建
            </el-button>
            <el-button @click="dialogFormVisible = false" type="primary">
              编辑
            </el-button>
          </el-form-item>
        </div>
      </el-form>
      <el-divider></el-divider>
      <div slot="footer" class="dialog-footer">
        <el-button @click="dialogFormVisible = false"> 取消 </el-button>
        <el-button
          type="primary"
          @click="dialogStatus === 'create' ? createData() : updateData()"
        >
          确认
        </el-button>
      </div>
    </el-dialog>
  </div>
